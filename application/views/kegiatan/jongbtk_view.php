                            <p>
                                 Jong Bataks Art Festival merupakan sebuah pagelaran budaya Batak dan Sumatera yang dirangkai dengan pertunjukan seni budaya yakni, musik, tari, teater, sastra, rupa, film, dan workshop (diskusi). Terdiri dari etnik Batak itu sendiri yaitu, Toba, Mandailing, Karo, Pak-pak, Simalungun, Angkola, dan Nias.<br>
								Jong Batak Art Festival merupakan embrio dari Sumpah Pemuda yang pada 1926 didirikan oleh Sanusi Pane, Amir Syarifuddin Harahap, dan yang lainnya dalam nama Jong Bataks Bond yang bertujuan mempereret persatuan dan persaudaraan di antara para pemuda yag berasal dari daerah Tapanuli dan Sumatera serat turut serta memajukan kebudayaan daerah. 

